<h2><a name="websites"></a><img src="/images/public.svg" alt="Website icon"/>Websites</h2>
<ul>
    <li><a target="_blank" rel="noopener noreferrer" href="https://ingress.plus">Ingress.Plus</a>
        <ul>
            <li>The very website you are already on :)</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://community-archive.ingress.plus">Community Forums Archive</a>
            <ul>
                <li>Our Archive of the now defunct Ingress Community Forums</li>
            </ul>
        </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://ingress.com">Ingress</a>
        <ul>
            <li>The official Websites of Ingress, includes<ul>
                    <li><a target="_blank" rel="noopener noreferrer" href="https://ingress.com/news">News &amp; Events</a>
                        <ul>
                            <li>The main place to get game news</li>
                        </ul>
                    </li>
                    <li><a target="_blank" rel="noopener noreferrer" href="https://intel.ingress.com/">Intel Map</a>
                        <ul>
                            <li>Use the Intel map to see the status of Portals, Links and Fields globally</li>
                        </ul>
                    </li>
                    <li><a target="_blank" rel="noopener noreferrer" href="https://ingress.com/support">Support</a>
                        <ul>
                            <li>Read through the knowledge base or open a support ticket</li>
                        </ul>
                    </li>
                    <li><a target="_blank" rel="noopener noreferrer" href="https://missions.ingress.com/">Mission Creator</a>
                        <ul>
                            <li>Create Missions that Agents can play once they have been approved</li>
                        </ul>
                    </li>
                    <li><a target="_blank" rel="noopener noreferrer" href="https://www.youtube.com/@Ingress">YouTube</a>
                        <ul>
                            <li>YouTube Channel which receives new videos from time to time</li>
                        </ul>
                    </li>
                </ul>
            </li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://nianticspatial.com/recon">Niantic Spatial Recon</a>
        <ul>
            <li>Review and edit your Portal Submission before they go into voting</li>
            <li>Review other submissions and earn points towards your Recon medal in Ingress</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://iitc.app/">IITC-CE</a>
        <ul>
            <li>Script to allow plugins for the Intel Map</li>
            <li>Works for Desktop, Android and iOS</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://bannergress.com">Bannergress</a>
        <ul>
            <li>Lists and searches through Mission banners submitted by Agents</li>
            <li>Map can also be used to see which banners are around you</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://www.openbanners.org/">OpenBanners</a>
        <ul>
            <li>Alternative, open-source front-end for Bannergress's back-end</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://fevgames.net/category/ingress/">FevGames</a>
        <ul>
            <li>News site that covers Ingress from time to time</li>
            <li>Mainly known <a target="_blank" rel="noopener noreferrer" href="https://fevgames.net/ingressfs/">for organizing the Ingress First Saturday events</a></li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://www.ingress-cards.com/">Ingress Biocards</a>
        <ul>
            <li>Database of Biocards and Swag, both official and unofficial</li>
            <li>Also has <a target="_blank" rel="noopener noreferrer" href="https://www.ingress-cards.com/badges/">a handy guide</a> to check which Biocard gives what medal</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://softspot.nl/ingress/bannercropper/">Mission Banner Cropper</a>
        <ul>
            <li>Crops and splits images so that they can be used as a banner</li>
            <li>Defacto successor of this <a target="_blank" rel="noopener noreferrer" href="https://www.giacintogarcea.com/ingress/tools/missionset/">Mission Banner Cropper</a> as it hasn't been updated in a while</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://missionday.site/">Mission Day Sites</a>
        <ul>
            <li>Lists all upcoming Mission Day events and their details</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="http://glyphtionary.com/">The Glyphtionary</a>
            <ul>
                <li>Searchable database of every known Glyph, be it in the game or story only</li>
            </ul>
        </li>
</ul>
