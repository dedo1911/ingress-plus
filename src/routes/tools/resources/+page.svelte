<h3><a name="tg-bots"></a>Bots</h3>
<p>This section lists Telegram bots that are great to have in your repertoire.</p>
<p>Note: “Inline” bots can be used in any chat by typing the username of the bot at the beginning of a message. Some bots work inline only.</p>
<ul>
    <li><a target="_blank" rel="noopener noreferrer" href="https://t.me/drone_reminder_bot">Ingress Drone Reminder</a>
        <ul>
            <li>Reminds you to move your drone after an hour (or whatever the current cooldown during an event is)</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://t.me/Bannergressbot">Bannergress Bot</a>
        <ul>
            <li>Can be used to search through the database or show you banners near your location</li>
        </ul>
    </li>
    <li><a target="_blank" rel="noopener noreferrer" href="https://t.me/IngressProfileBot">IngressProfileBot - @IngressProfileBot</a> - Inline only<ul>
            <li>Type an Agents name and instantly share a link to their profile in the chat</li>
        </ul>
    </li>
</ul>

