    <h3>1.1 When Not Registered (Visitors)</h3>
    <p>When you access Ingress Plus without registering an account, the following data may be collected automatically:</p>
    <ul>
        <li>IP address</li>
        <li>Date and time a resource was requested</li>
        <li>User agent of your browser (Browser type, version, Operating System)</li>
        <li>URL of the requested resource</li>
        <li>URL of the referring page (Referrer)</li>
    </ul>

    <h3>1.2 When Registered (Users with Account)</h3>
    <p>When you register or log in via OAuth (currently supported provider: <a href="https://policies.google.com/?hl=en" target="_blank" rel="noopener noreferrer">Google</a>),
      we additionally collect the following data as provided by the OAuth service:</p>
    <ul>
        <li>Email address</li>
        <li>URL to your avatar</li>
        <li>Display name</li>
        <li>OAuth provider name and OAuth token</li>
    </ul>
    <p>Additionally, if set by the user:</p>
    <ul>
        <li>Agent name</li>
        <li>Faction</li>
    </ul>
    <p>Further data may be collected depending on which site features you use, for example:</p>
    <ul>
        <li>Acquired badges</li>
        <li>Text and pictures submitted via bug reports</li>
        <li>Supporter Status</li>
    </ul>

    <h3>1.3 When Submitting Media via the IITC Mediagress Plugin</h3>
    <p>When using the Mediagress plugin, we collect the following data:</p>
    <ul>
        <li>Ingress username as reported by IITC</li>
        <li>Media item data, including:
            <ul>
                <li>Timestamp of when the Media was acquired</li>
                <li>Internal Ingress player ID</li>
            </ul>
        </li>
        <li>IP address</li>
        <li>Date and time a resource was requested</li>
        <li>User agent of your browser (Browser type, version, Operating System)</li>
        <li>URL of the requested resource</li>
        <li>URL of the referring page (Referrer)</li>
    </ul>

    <h3>1.4 Data Stored on Your System</h3>
    <p>Ingress Plus may store the following data on your device:</p>
    <ul>
        <li>Cookies (for authentication)</li>
        <li>Site settings</li>
        <li>Timestamp of your last Media upload (plugin)</li>
        <li>List of uploaded Media (plugin)</li>
    </ul>

